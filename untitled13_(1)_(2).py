"""#16. Top N-gram per Sentimen"""

def plot_ngram(corpus, n, label, top_k=5):
    vec = CountVectorizer(ngram_range=(n, n)).fit(corpus)
    bag = vec.transform(corpus)
    freq = bag.sum(axis=0)
    words_freq = sorted([(w, freq[0,i]) for w,i in vec.vocabulary_.items()],
                        key=lambda x: x[1], reverse=True)[:top_k]
    words, counts = zip(*words_freq)
    gram = 'Unigram' if n==1 else 'Bigram'

    # Warna: positif=hijau, negatif=merah
    bar_color = '#4caf7d' if label == 'positif' else '#e05c5c'

    fig, ax = plt.subplots(figsize=(9,4))
    bars = ax.barh(list(reversed(list(words))),
                   list(reversed(list(counts))),
                   color=bar_color, edgecolor='white', linewidth=0.5)
    for bar, count in zip(bars, reversed(list(counts))):
        ax.text(bar.get_width()+0.5, bar.get_y()+bar.get_height()/2,
                str(count), va='center', fontsize=10, fontweight='bold')
    ax.set_title(f'Top-{top_k} {gram} — Sentimen {label.capitalize()}', fontweight='bold')
    ax.set_xlabel('Frekuensi')
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    plt.tight_layout()
    plt.savefig(f'{gram.lower()}_{label}.png', dpi=150, bbox_inches='tight')
    plt.show()
    return words_freq

