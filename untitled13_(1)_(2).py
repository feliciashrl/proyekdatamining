# ── 4. Tokenizing ────────────────────────────────────────
def tokenizingText(text):
    return word_tokenize(text)

# ── 5. Stopword Removal ──────────────────────────────────
def filteringText(text):
    list_sw = set(stopwords.words('indonesian'))
    list_sw.update(set(stopwords.words('english')))
    list_sw.update(['iya','yaa','gak','nya','na','sih','ku','di',
                    'ga','ya','gaa','loh','kah','halodoc','app',
                    'aplikasi','woi','woii','woy','min','dok',
                    'halo','lah','deh','nih','dong'])
    return [w for w in text if w not in list_sw]

# ── 6. Stemming ──────────────────────────────────────────
factory = StemmerFactory()
stemmer = factory.create_stemmer()

exception_dict = {
    'perasaan':'perasaan','pelayanan':'pelayanan',
    'permasalahan':'permasalahan','pembayaran':'pembayaran',
    'kesehatan':'kesehatan','kemudahan':'kemudahan',
    'keamanan':'keamanan','kepuasan':'kepuasan',
    'keluhan':'keluhan','pertanyaan':'pertanyaan',
    'konsultasi':'konsultasi','pelayanan':'pelayanan',
}

def stemmingText(word_list):
    result = []
    for w in word_list:
        if w in exception_dict:
            result.append(exception_dict[w])
        else:
            result.append(stemmer.stem(w))
    return result

# ── 7. Join ke kalimat ───────────────────────────────────
def toSentence(list_words):
    return ' '.join(list_words)

